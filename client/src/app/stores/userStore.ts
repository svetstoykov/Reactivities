import { makeAutoObservable } from "mobx";
import { history } from "../..";
import { LoginApiModel } from "../../autogenerated/a-p-i/common/identity/models/login-api-model";
import { ProfileApiModel } from "../../autogenerated/a-p-i/profiles/models/profile-api-model";
import { RegisterApiModel } from "../../autogenerated/a-p-i/common/identity/models/register-api-model";
import { UserApiModel } from "../../autogenerated/a-p-i/common/identity/models/user-api-model";
import agent from "../api/agent";
import { store } from "./store";

export default class UserStore {
    private user: UserApiModel | null = null;

    constructor() {
        makeAutoObservable(this);
    }

    get IsLoggedIn() {
        return !!this.user;
    }

    get currentUser() {
        return this.user;
    }

    login = async (creds: LoginApiModel) => {
        try {
            const user = await agent.Accounts.login(creds);

            this.setUserAndCloseModal(user);
        } catch (error) {
            console.log(error);
        }
    };

    register = async (details: RegisterApiModel) => {
        try {
            const user = await agent.Accounts.register(details);

            this.setUserAndCloseModal(user);
        } catch (error) {
            console.log(error);
        }
    };

    setUser = (user: UserApiModel | null) => {
        this.user = user;
    };

    logout = () => {
        store.commonStore.setToken(null);
        window.localStorage.removeItem("userToken");
        this.setUser(null);
        history.push("/");
    };

    isUserActivityHost = (activityHostUsername: string | undefined) => {
        return this.user?.username === activityHostUsername;
    };

    isUserGoingToActivity = (activityId: number) => {
        return store.activityStore.activitiesRegistry.get(activityId)
            ?.attendees.some((a) => a.username === this.user?.username);
    };

    loadCurrentUser = async () => {
        try {
            const user = await agent.Accounts.currentUser();
            this.setUser(user);
        } catch (error) {
            console.log(error);
        }
    };

    getUserProfileModel = (user: UserApiModel) => {
        const profileApiModel: ProfileApiModel = {
            displayName: user.displayName,
            username: user.username,
            image: user.image,
            bio: "",
        };

        return profileApiModel;
    };

    private setUserAndCloseModal = (user: UserApiModel) => {
        if (user) {
            store.commonStore.setToken(user.token);
            this.setUser(user);
        }

        history.push("/activities");
        store.modalStore.closeModal();
    };
}
